Comment1 = sprintf('%s\n', ...
'                 ISCCP D2 Cloud Data Product (C2 is an older version of this dataset)', ...
' ', ...
'    A combination of satellite-measured radiances, TOVS atmospheric temperature/humidity and ',...
' ice/snow data are used by ISCCP to produce a global dataset on cloud and surface variables. ',...
' An overview of the Project and the data products is given in Rossow and Schiffer (1991); the ',...
' algorithm and its effectiveness are described by Rossow and Garder (1993a&b) while Rossow et ',...
' al. (1993) compare the resulting products to other cloud climatologies. The ISCCP D-series, ',...
' which is described in this readme and more extensively in Rossow et al. (1996), is a revised ',...
' version of the C-series dataset. The ISCCP D2-series products are gridded data averaged over ',...
' each month. These data set(presently covering the period 1986-1987 & 1989-1992) are originally ',...
' produced on an equal area map grids which has a constant 2.5 degree latitude increments and ',...
' varaiable longitude increments ranging from 2.5 degree at the equator to 120 degree at the pole. ',...
' The Goddard DAAC has regridded a subset (36 out of the original 130 parameters) of these dataset ',...
' to 1x1 degree equal angle grid for inclusion in the Interdisciplinary data collection.');

Comment2 = sprintf('%s\n', ...
' ',...
'    The original ISCCP D1 (daily) and D2 (monthly mean) products were calculated on an ',...
' approximately equal area world grid (280x280 km^2) which is equivalent to a 2.5x2.5 degree ',...
' latitude-longitude grid at the equator. The latitude band widths were fixed to 2.5 degree ',...
' and grid size along the longitude was varied to provide an integer number of cells in a ',...
' latitude zone and grid cell area as close to an equatorial grid cell as possible. Map grids ',...
' started from south pole to north pole. In each latitude zone, all longitudes were indexed in ',...
' order from the Greenwich meridian eastward (0 - 360 degree) before going to the next latitude ',...
' zone. Here we have interpolated a subset (36 out of the original 130 parameters) of the equal ',...
' area monthly mean products to a 1x1 degree grid for easy comparison to the other ',...
		   ' Interdisciplinary Data Collection products.');

